def detect_bank_from_card(card_digits: str) -> str:
    d = re.sub(r"\D", "", card_digits or "")
    if len(d) < 6:
        return ""
    return CARD_BIN_TO_BANK.get(d[:6], "")


def detect_bank_from_sheba(sheba: str) -> str:
    s = re.sub(r"\s+", "", (sheba or "").upper())
    if not s.startswith("IR") or len(s) < 5:
        return ""
    code = s[4:7]
    return SHEBA_BANK_CODE.get(code, "")


def detect_bank_from_text(text: str) -> str:
    low = (text or "").lower()
    for kw, name in _BANK_KEYWORDS:
        if kw.lower() in low:
            return name
    return ""
